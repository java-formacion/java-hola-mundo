package com.ipartek.formacion.ejercicios.principiantes;

/**
 * Programa java que realice lo siguiente: declarar dos variables X e Y de tipo
 * int, dos variables N y M de tipo double y asigna a cada una un valor. A
 * continuación reliza y muestra por pantalla una serie de operaciones entre
 * ellas.
 * 
 * @author java
 *
 */
public class Ejercicio2 {

	public static void main(String[] args) {
		int x = 1;
		int y = 2;
		double n = 3.2;
		double m = 4.7;

		System.out.println("La variable x tiene el valor: " + x);
		System.out.println("La variable y tiene el valor: " + y);
		System.out.println("La variable n tiene el valor: " + n);
		System.out.println("La variable m tiene el valor: " + m);
		System.out.println("-------------------------------------");
		System.out.println("La suma de x+y es: " + (x + y));
		System.out.println("La resta de x-y es: " + (x - y));
		System.out.println("La multiplicacion de x*y es: " + (x * y));
		System.out.println("La division de x/y es: " + (x / y));

	}

}
