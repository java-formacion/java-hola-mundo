package com.ipartek.formacion.ejercicios.Capitulo3.Practica2.Ejercicio1;

public class Ave {

	private char sexo;
	private int edad;
	
	public Ave(char sexo, int edad) {
		super();
		this.sexo = sexo;
		this.edad = edad;
	}

	public char getSexo() {
		return sexo;
	}

	public void setSexo(char sexo) {
		this.sexo = sexo;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	
}
