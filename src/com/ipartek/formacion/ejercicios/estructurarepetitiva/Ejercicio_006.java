package com.ipartek.formacion.ejercicios.estructurarepetitiva;

/**
 * 
 * Ejemplo de for. 
 * 
 * Programa Java que muestre los números del 100 al 1 utilizando la instrucción for.
 *
 */

public class Ejercicio_006 {

	public static void main(String[] args) {
		
        System.out.println("Numeros del 100 al 1: ");
        
        for(int i=100;i>=1;i--)
            System.out.println(i);
	}
}
