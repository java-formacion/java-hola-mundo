package ejercicios.iterativos;

/**
 * Mostrar los números del 1 al 100 utilizando un bucle do while
 * 
 * @author eritzinho
 *
 */
public class Ejercicio2 {

	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<=100);
	}

}
